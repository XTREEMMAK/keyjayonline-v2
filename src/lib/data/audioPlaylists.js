// Helper function to get all tracks as a flat array
export function getAllTracks() {
	return Object.values(audioPlaylists).flatMap(playlist => playlist.tracks);
}

// Helper function to get tracks by genre
export function getTracksByGenre(genre) {
	const playlist = audioPlaylists[genre.toLowerCase()];
	return playlist ? playlist.tracks : [];
}

// Helper function to get a specific playlist
export function getPlaylist(playlistKey) {
	return audioPlaylists[playlistKey] || null;
}